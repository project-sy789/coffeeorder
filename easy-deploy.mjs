function runCommand(command, returnOutput = false) {
  try {
    log(`\n‡∏£‡∏±‡∏ô‡∏Ñ‡∏≥‡∏™‡∏±‡πà‡∏á: $ ${command}`, "gray");
    
    if (returnOutput) {
      // ‡πÉ‡∏ä‡πâ execSync ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏£‡∏±‡∏ô‡∏Ñ‡∏≥‡∏™‡∏±‡πà‡∏á‡πÅ‡∏ö‡∏ö‡∏ã‡∏¥‡∏á‡πÇ‡∏Ñ‡∏£‡∏ô‡∏±‡∏™‡πÅ‡∏•‡∏∞‡∏£‡∏±‡∏ö‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå
      const stdout = execSync(command, { encoding: 'utf8' });
      return stdout;
    } else {
      // ‡πÉ‡∏ä‡πâ execSync ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏£‡∏±‡∏ô‡∏Ñ‡∏≥‡∏™‡∏±‡πà‡∏á‡πÅ‡∏ö‡∏ö‡∏ã‡∏¥‡∏á‡πÇ‡∏Ñ‡∏£‡∏ô‡∏±‡∏™‡πÅ‡∏•‡∏∞‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå
      execSync(command, { stdio: 'inherit' });
      return true;
    }
  } catch (error) {
    console.error(error);
    return false;
  }
}

